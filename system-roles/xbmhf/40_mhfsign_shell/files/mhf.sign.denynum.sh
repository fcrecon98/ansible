#!/bin/bash
######################################################
# 名称：MHF用signサーバの国外拒否件数取得シェル
# 仕様：直近で更新のあったログファイルに対し"deny"の件数をカウント
# 引数：なし
# ---------------------
# 2016/05/10 新規作成 by 林 如弥
#####################################################

find /var/log/mhf/cur/sgn -mmin -60 -type f -name '*.log' \
  | xargs grep -hc deny \
  | awk '
      {sum+=$1};END{print sum}
    '

exit 0

